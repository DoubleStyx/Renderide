//! Finger-pose presets used by hand synthesis.
//!
//! The arrays are calibration data; keep the literal values stable for diffability.
//! Position and rotation entries are local to the previous segment in each finger so curl
//! interpolation happens in finger hierarchy space before being flattened for IPC.

#![expect(
    clippy::unreadable_literal,
    reason = "preset literals intentionally match host hand-pose calibration data"
)]

/// Number of fingers described by the preset tables.
pub(super) const FINGER_COUNT: usize = 5;
/// Maximum number of segments stored for any finger.
pub(super) const MAX_SEGMENTS_PER_FINGER: usize = 5;
/// Local finger segment position table.
pub(super) type LocalPositionPresets = [[[f32; 3]; MAX_SEGMENTS_PER_FINGER]; FINGER_COUNT];
/// Local finger segment rotation table.
pub(super) type LocalRotationPresets = [[[f32; 4]; MAX_SEGMENTS_PER_FINGER]; FINGER_COUNT];
/// Number of finger segments per finger: thumb, index, middle, ring, pinky.
pub(super) const SEGMENT_COUNTS: [usize; FINGER_COUNT] = [4, 5, 5, 5, 5];
/// Number of finger segments in a [`HandState`]. Equal to
/// `BodyNode::LeftPinkyTip - BodyNode::LeftThumbMetacarpal + 1 = 42 - 19 + 1`.
